package io.compact.kafka

/** Стратегия обработки несовместимых сообщений.
 *
 * Применяется когда консьюмер получает сообщение версия которого
 * несовместима с его minimumVersion.
 *
 * Выбор стратегии зависит от требований к надёжности:
 *  - [[Fail]]       — строгий режим, нулевая потеря данных
 *  - [[Skip]]       — мягкий режим, старые сообщения игнорируются
 *  - [[DeadLetter]] — архивный режим, несовместимые сообщения сохраняются для анализа
 */
enum IncompatibilityStrategy:

  /** Завершить поток с ошибкой [[io.compact.core.ContractError.IncompatibleVersion]].
   * Подходит для критичных pipeline где потеря сообщения недопустима.
   */
  case Fail

  /** Пропустить несовместимое сообщение, залогировать предупреждение.
   * Подходит когда старые сообщения не важны (например, события UI).
   */
  case Skip

  /** Направить несовместимое сообщение в dead letter queue топик.
   * Основной pipeline продолжает работу, несовместимые сообщения
   * доступны для анализа и повторной обработки.
   *
   * @param dlqTopic Имя DLQ топика
   *
   * V1: логирует с указанием dlqTopic.
   * V2: реальная отправка в Kafka DLQ топик.
   */
  case DeadLetter(dlqTopic: String)
