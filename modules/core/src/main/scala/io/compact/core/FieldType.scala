package io.compact.core

/** Тип поля контракта.
 *
 * Необязательность поля контролируется через [[Field.required]],
 * не через тип. FieldType описывает только форму данных.
 *
 * Важно для версионирования:
 *  - Добавить вариант в [[Union]]  → Minor изменение
 *  - Убрать вариант из [[Union]]   → Major изменение (breaking)
 *  - Любое другое изменение типа  → Major изменение (breaking)
 */
enum FieldType:

  // ── Примитивы ──────────────────────────────────────────────────────────────

  /** UTF-8 строка */
  case Str

  /** 32-битное целое число со знаком */
  case Int32

  /** 64-битное целое число со знаком */
  case Int64

  /** 32-битное число с плавающей точкой */
  case Float32

  /** 64-битное число с плавающей точкой */
  case Float64

  /** Булево значение */
  case Bool

  /** Бинарные данные (base64 в JSON) */
  case Bytes

  // ── Семантические типы ─────────────────────────────────────────────────────

  /** Временная метка — ISO 8601, хранится как строка */
  case Timestamp

  /** UUID — строка в формате "xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx" */
  case Uuid

  // ── Составные типы ─────────────────────────────────────────────────────────

  /** Упорядоченный список элементов одного типа */
  case Collection(element: FieldType)

  /** Словарь (key → value). Ключ обычно Str или Uuid. */
  case Mapping(key: FieldType, value: FieldType)

  /** Ссылка на другой контракт — вложенный объект.
   *
   * Реестр отслеживает эту зависимость: если контракт B изменился,
   * реестр знает что контракт A на него ссылается.
   */
  case Nested(contractId: ContractId)

  /** Union type — поле может быть одним из нескольких типов.
   *
   * Соответствует discriminated union / oneOf в JSON Schema.
   *
   * @param variants список допустимых типов, порядок не важен
   */
  case Union(variants: List[FieldType])
